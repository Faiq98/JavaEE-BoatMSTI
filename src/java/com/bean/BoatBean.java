package com.bean;

public class BoatBean {
    private String boatID;
    private TourismCompanyBean companyID;
    private String boatType;
    private int boatCapacity;

    public String getBoatID() {
        return boatID;
    }

    public void setBoatID(String boatID) {
        this.boatID = boatID;
    }

    public TourismCompanyBean getCompanyID() {
        return companyID;
    }

    public void setCompanyID(TourismCompanyBean companyID) {
        this.companyID = companyID;
    }

    public String getBoatType() {
        return boatType;
    }

    public void setBoatType(String boatType) {
        this.boatType = boatType;
    }

    public int getBoatCapacity() {
        return boatCapacity;
    }

    public void setBoatCapacity(int boatCapacity) {
        this.boatCapacity = boatCapacity;
    }
    
        
}
